APP_HOWTO = """
## 🧭 Comment utiliser JobCompass ?

Voici les étapes pour exploiter pleinement l'outil :

### 1. 📁 Importer vos données
Depuis la barre latérale :
- Cliquez sur `🔼 Charger un CSV` pour importer vos offres d’emploi ou de mission (format `.csv`).
- Cliquez sur `💾 Télécharger le CSV` pour récupérer vos données enrichies ou filtrées.

> 📌 **Format requis :** fichier `.csv`, séparateur `|`, avec des colonnes telles que `Date`, `Type`, `Marché`, `Technos principales`, etc.

---

### 2. 📈 Analyser les marchés
Dans l’onglet **Analyse des Marchés** :
- Visualisez l’évolution du nombre d’annonces par secteur.
- Repérez les périodes de tension ou d’accalmie pour adapter votre stratégie de prospection.

---

### 3. 🔎 Dissecter les offres
Dans l’onglet **Dissection des Offres** :
- Explorez chaque annonce individuellement : rythme, localisation, TJM, séniorité, etc.
- Comparez les attentes entre marchés pour mieux ajuster votre profil.

---

### 4. 🧭 Utiliser la Boussole
Dans l’onglet **Boussole** :
- Sélectionnez un marché ou un poste cible (ex. : *Développeur React*, *DevOps*).
- Visualisez les **technologies et compétences clés** les plus fréquentes pour optimiser votre CV, vos pitchs ou vos formations à venir.
"""

